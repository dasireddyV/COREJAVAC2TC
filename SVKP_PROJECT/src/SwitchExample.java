
public class SwitchExample {

	public static void main(String[] args) {
		int x=2;
		switch(x) {
		case 1:
			System.out.println("value is 1");
			break;
		case 2:
			System.out.println("value is 2");
			break;
		case 3:
			System.out.println("value is 3");
			break;
		default:
			System.out.println("value is other than 1,2,3");
		}
	}

}
